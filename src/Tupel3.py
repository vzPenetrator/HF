# Fehlerhafter Versuch zur Änderung eines Tupels
zahlen = (2, 3, 5, 7, 11)   # Erstellt ein Tupel `zahlen` mit den Werten: 2, 3, 5, 7, 11.
zahlen[0] = 6               # Versucht, das erste Element des Tupels zu ändern, was nicht erlaubt ist, da Tupel unveränderlich sind.
print(zahlen)               # Wird nicht ausgeführt, weil ein Fehler auftritt.