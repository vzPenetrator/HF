i = 0                       # Initialisiert die Variable `i` mit dem Wert 0.
while i < 10:               # Startet eine Schleife, die ausgeführt wird, solange `i` kleiner als 10 ist.
    i += 1                  # Erhöht den Wert von `i` um 1 in jedem Schleifendurchlauf.
    print(i, ":", i * i)    # Gibt den aktuellen Wert von `i` und dessen Quadrat aus.
print("Schleife beendet")   # Gibt aus, dass die Schleife beendet wurde, nachdem die Bedingung nicht mehr erfüllt ist.