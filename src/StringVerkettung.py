var_1 = "Hallo"                 # Weist der Variablen 'var_1' den String "Hallo" zu.
var_2 = 1                       # Weist der Variablen 'var_2' den Wert 1 (eine Ganzzahl) zu.
var_3 = "1"                     # Weist der Variablen 'var_3' den String "1" zu.

print(var_1 + str(var_2))       # Wandelt 'var_2' (1) in einen String um und verkettet ihn mit 'var_1' ("Hallo"), Ergebnis: "Hallo1".
print(str(var_2) + var_3)       # Wandelt 'var_2' (1) in einen String um und verkettet ihn mit 'var_3' ("1"), Ergebnis: "11".