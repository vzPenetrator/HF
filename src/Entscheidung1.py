a = 8                   # Variable `a` wird mit dem Wert 8 initialisiert.
if a == 2:              # Überprüft, ob `a` gleich 2 ist.
    print("s")          # Gibt "s" aus, wenn die Bedingung wahr ist.
elif a == 8:            # Überprüft, ob `a` gleich 8 ist, wenn die vorherige Bedingung falsch war.
    print("elif")       # Gibt "elif" aus, wenn diese Bedingung wahr ist.
else:                   # Führt den Code im `else`-Block aus, wenn keine der vorherigen Bedingungen wahr ist.
    if a == 5:          # Überprüft, ob `a` gleich 5 ist.
        print("asdf")   # Gibt "asdf" aus, wenn `a` gleich 5 ist.
